with tab_b:
    st.subheader("Model B (B2B2C): пациент платит клинике, клиника делится с ReFlex")
    st.markdown(
        "ReFlex предоставляет устройства и ПО клинике **бесплатно** (или по минимальной цене). "
        "Клиника сдаёт устройства пациентам в аренду и передаёт ReFlex долю от арендной платы. "
        "Прямой плательщик ReFlex — клиника (но источник денег — пациент)."
    )

    st.markdown("### Схема денежного потока")
    st.markdown(
        """
```
Пациент приходит в клинику
  │
  └─► Платит rental_price/мес за аренду устройства
        │
        ├─► Клиника забирает: rental_price × clinic_commission_rate  (комиссия)
        │
        └─► ReFlex получает: rental_price × (1 − clinic_commission_rate)  = Net Revenue
              └─► Из Net Revenue покрываются все затраты (Fixed + Variable)
```
"""
    )

    st.markdown("### Ключевые формулы")
    col1, col2 = st.columns(2)
    with col1:
        st.latex(
            r"\text{New Patients}_m = \begin{cases}"
            r"n_{\text{clinics}} \times p_{\text{m1}} & m = 1 \\"
            r"\text{New Patients}_{m-1} \times (1 + g) & m > 1"
            r"\end{cases}"
        )
        st.caption("n_clinics — кол-во клиник, p_m1 — пациентов на клинику в месяц 1.")
        st.latex(
            r"\text{Cohort Active}_m = \sum_{k=\max(1,\,m-D+1)}^{m} \text{New Patients}_k"
        )
        st.caption("D = rehab_duration_months.")
        st.latex(
            r"\text{Effective}_m = \lfloor \text{Cohort Active}_m \times (1 - c) \times u \rfloor"
        )
        st.caption("c = churn_rate, u = utilization_rate.")
    with col2:
        st.latex(r"\text{Gross Revenue}_m = \text{Effective}_m \times \text{rental\_price}")
        st.latex(
            r"\text{Net Revenue}_m = \text{Gross Revenue}_m \times (1 - \text{commission\_rate})"
        )
        st.latex(
            r"\text{Cash Flow}_m = \text{Net Revenue}_m - \text{Fixed Costs}_m - \text{Variable Costs}_m"
        )

    st.markdown("### Парк устройств")
