col_left, col_right = st.columns(2)

with col_left:
    with st.expander("💵 Что такое Cash Flow и как он считается", expanded=True):
        st.latex(
            r"\text{CF}_m = \text{Revenue}_m - \text{Fixed Costs}_m - \text{Variable Costs}_m"
        )
        st.markdown(
            """
**Revenue** — всё, что заработал ReFlex в месяце m (Setup Fee + Subscription + Net Rental в зависимости от модели).

**Fixed Costs** — постоянные затраты, не зависящие от объёма:
- Зарплаты команды
- Аренда офиса / коворкинга
- Серверная инфраструктура (фиксированная часть)
- Юридические услуги
- Прочие постоянные расходы

**Variable Costs** — переменные затраты, растущие с объёмом:
- COGS (себестоимость устройства) — при каждом производстве новых устройств
- Логистика — при появлении нового пациента (доставка туда-обратно)
- Поддержка — каждый месяц на каждого активного пациента
- CAC клиники — при подключении новой клиники
- CAC пациента — при появлении нового пациента (если задан)
- Переменная инфраструктура — пропорционально активным пользователям
"""
        )

    with st.expander("📈 Как считается безубыточность (Breakeven)", expanded=True):
        st.latex(
            r"\text{Cumulative CF}(N) = \sum_{m=1}^{N} \text{CF}_m"
        )
        st.markdown(
            """
**Breakeven** — месяц N, при котором Cumulative CF(N) ≥ 0 впервые.

До этого момента компания суммарно тратит больше, чем зарабатывает.
После — накопленная прибыль перекрывает накопленные убытки.

**Целевая безубыточность** — инструмент обратного расчёта:
задаёте желаемый месяц N, и модель находит минимальную цену (аренды или подписки),
при которой CumCF(N) ≥ 0.
"""
        )

with col_right:
    with st.expander("👥 Churn и загрузка (utilization)", expanded=True):
        st.latex(r"\text{Effective}_m = \lfloor \text{Cohort Active}_m \times (1 - c) \times u \rfloor")
        st.markdown(
            """
**Churn rate (c)** — доля пациентов, которые прекращают использование устройства раньше срока реабилитации
(отказ от программы, смена клиники, форс-мажор). Например, при c = 10%: из 100 пациентов когорты
эффективно задействованы только 90.

**Utilization rate (u)** — коэффициент фактической загрузки устройств.
Даже при 100 активных пациентах часть устройств может простаивать (техобслуживание, резерв).
При u = 0.9 из 100 пациентов в выручке участвуют 90.

Оба коэффициента применяются **одновременно**: это умножает их эффект.
"""
        )

    with st.expander("🔒 Почему парк устройств не уменьшается", expanded=True):
        st.markdown(
            """
Устройства — **капитальный актив**: однажды произведённые, они остаются в парке до окончания срока службы.
Пациент завершил реабилитацию → устройство освобождается и **доступно новому пациенту**,
но из парка не исчезает.

Поэтому `devices_in_pool` монотонно растёт: новые устройства добавляются при росте demand,
но никогда не списываются (в рамках горизонта планирования модели).

Это означает: **Subscription Revenue никогда не падает** — подписка считается с полного парка каждый месяц.
"""
        )

